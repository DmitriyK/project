package com.test.project.domain.common;

import javax.persistence.*;

@MappedSuperclass
public abstract class Common {

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
